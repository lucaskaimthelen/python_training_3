# Tuple is a sequence of immutable Python objects. Tuples cannot be changed
# once they have been created.

w = (1, 2, 3, 4, 5, 6, 7, 8)
# Retruns the minimum value in the tuple.
print(min(w))

# Retruns the maximum value in the tuple.
print(max(w))

# Len function.
print(len(w))

# Print tuple value at position x.
print(w[1])
print(w[0])

# What is wrong with this?
w.append(10)