# Variable substitution.
i = 100
print(i)

# Variable substitution.
i = 2
print(i)

# Reassign variable value.
a = [1, 2, 3]
b = a
print(b)
b = 5
print(b)

# Multiple Assignment.
a = b = c = d = 5
print(d)

# Type case a float to int.
w = 5.0
print(int(w))

# Addition
c = 5 + 10
print(c)

# Subtraction.
d = 5 - 3
print(d)

# Multiplication
a = 5 * 5

# Division with two integers.
a = 5/3
print(a)

b = 5/3.0
print(b)

c = float(5)/3
print(c)

# Floor division divides one number by another and then rounds the results to
# the closest integer that is smaller.
10//3

# Modulus. Returns remainder.
c = 14%3
print(c)

# Exponent
b = 2**2
print(b)

# Semicolon can be used to terminate statements. They are not required.
# Semicolon can be used to create Compound statements. This allows one to have
# multiple statements on the same line. 
a = 10; b = 5
print(a, b)
